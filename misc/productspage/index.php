<h2>Description</h2>

<p>The Captive Shoulder Pin Assembly is an innovative pin that incorporates a Captive Bushing to prevent it from being completely withdrawn from the tool. The shoulder is intended to eliminate pinch point when the pin is deployed. L-Pin and T-Pin handle configurations are available to suit any application. Captive Bolts come as a complete assembly including the Bolt, Bushing, and LS-1 Lockscrew. </p>

<p>Please Note: We have recently updated these products to enhance operator comfort and ergonomics. Some handle sizes may have changed. </p>

<p><strong>Material:</strong> Hardened Steel 
