<h2 class="exclamation">Engineering Tip</h2>

<p>In this application, the item on the left is an original, old design L-Pin which have brazed on handles that are prone to separating from the pin. This often results in broken pieces, frustrated operators, and possible injury. The Captive Shoulder Pin as shown on the right eliminates these issues as it is a strong, one-piece design that is not assembled with brazing.</p>

<img src="images/productimg3.jpg" alt="">

</section>





<section>

<h2 class="exclamation">Engineering Tip 2</h2>

<p>In this application, the item on the left is an original, old design L-Pin which is designed with a slightly undersized diameter. This usually means that it must be used in conjunction with special bushings in the opposite plate for tight alignment. Special bushings are expensive and can have long lead times. The Captive Shoulder Pin as shown on the right eliminates this issue as the pin diameters are on-size so they can be used with standard bushings.</p>

<img src="images/productimg4.jpg" alt="">

</section>




<section>

<h2 class="exclamation">Engineering Tip 3</h2>

<p>In this application we demonstrate how the old style Captive L-Pins pose a "pinch-point" danger and how the Captive Shoulder T-Pins eliminates it. Users of the old Captive L-Pins will be able relate to the pain felt when their hand gets pinched between the L-Pin handle and the plate. The shoulder of the new Captive Shoulder Pins ensures that users can operate these pins without that worry, increasing productivity and comfort! </p>

<img src="images/productimg5.jpg" alt="">

</section>




